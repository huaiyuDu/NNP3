def print_weights_biases(weights,biases):
    for i in range(1,len(weights)):
        print(">>>>W" + str(i))
        print(weights[i])
        print(">>>>b" + str(i))
        print(biases[i])
def print_biases(biases):
    for i in range(1,len(biases)):
        print(">>>>b" + str(i))
        print(biases[i])
